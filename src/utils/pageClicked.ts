/**
 * 页面点击状态追踪工具
 * - 监听整个页面的点击事件
 * - 提供页面点击状态的全局状态管理
 * - 用于检测用户是否与页面有交互行为
 * - 帮助控制翻译弹窗的显示和隐藏逻辑
 */

// ===================== 页面点击状态管理 =====================

/** 全局页面点击状态标志 */
let pageClicked = false;

/**
 * 页面点击事件处理函数
 * - 当页面任意位置被点击时，将状态设置为true
 * - 用于记录用户与页面的交互行为
 */
const handlePageClick = () => {
  pageClicked = true
};

// ===================== 事件监听器绑定 =====================

/**
 * 为整个页面文档绑定点击事件监听器
 * - 使用document.documentElement确保捕获页面上所有区域的点击
 * - 包括页面背景、文本区域、按钮等各种可点击元素
 * - 为翻译扩展提供用户交互检测能力
 */
document.documentElement.addEventListener("click", handlePageClick);

// ===================== 状态导出 =====================

/**
 * 导出页面点击状态供其他模块使用
 * - 其他组件可以通过导入这个变量来检查用户是否与页面有交互
 * - 常用于控制弹窗显示时机、隐藏逻辑等交互场景
 */
export {pageClicked}